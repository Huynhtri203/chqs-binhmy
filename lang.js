document.addEventListener("DOMContentLoaded", () => {
  const langToggleBtn = document.getElementById('lang-toggle');
  let currentLang = localStorage.getItem('lang') || 'vi';
  
  const setLanguage = (lang) => {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (translations[lang] && translations[lang][key]) {
        // Sử dụng innerHTML để giữ các cấu trúc Icon lồng bên trong thẻ HTML
        el.innerHTML = translations[lang][key]; 
      }
    });
    
    // Đồng bộ cập nhật nhãn chữ trên nút chuyển đổi ngôn ngữ chính
    if(langToggleBtn) {
      langToggleBtn.innerHTML = lang === 'vi' ? '🇬🇧 EN' : '🇻🇳 VN';
    }
  };

  // Khởi tạo ngôn ngữ ban đầu cho trang web
  setLanguage(currentLang);

  // Lắng nghe sự kiện click chuyển đổi qua lại
  if (langToggleBtn) {
    langToggleBtn.addEventListener('click', () => {
      currentLang = currentLang === 'vi' ? 'en' : 'vi';
      localStorage.setItem('lang', currentLang);
      setLanguage(currentLang);
    });
  }
});